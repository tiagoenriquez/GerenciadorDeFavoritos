    /**
     * Salva as alterações dos dados de um favorito.
     * @param evt 
     */
    private void alterar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterar
        try {
            int id = Integer.parseInt(textFieldId.getText());
            String nome = textFieldNome.getText();
            String url = textFieldUrl.getText();
            String assunto = textFieldAssunto.getText();
            favoritoService.alterar(new Favorito(id, nome, url, assunto));
            principal.preencherListas();
            new Sucesso("Alterações salvas com sucesso").setVisible(true);
            fechar();
        } catch (Exception exception) {
            new Erro("Erro ao salvar as alterações\n" + exception.getMessage()).setVisible(true);
        }
    }//GEN-LAST:event_alterar

