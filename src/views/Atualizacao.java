    /**
     * Preenche os campos de digitação.
     * @param favorito 
     */
    private void preencherCampos(Favorito favorito) {
        Integer id = favorito.getId();
        textFieldId.setText(id.toString());
        textFieldNome.setText(favorito.getNome());
        textFieldUrl.setText(favorito.getUrl());
        textFieldAssunto.setText(favorito.getAssunto());
    }

    /**
     * Preenche o combo box com os assuntos dos favoritos.
     */
    private void preencherAssuntos() {
        try {
            List<String> assuntos = favoritoService.assuntos();
            comboBoxAssuntos.addItem("");
            assuntos.forEach((assunto) -> {
                comboBoxAssuntos.addItem(assunto);
            });
        } catch (Exception exception) {
            new Erro(exception.getMessage()).setVisible(true);
        }
    }
    
    /**
     * Seleciona o assunto do favorito.
     * @param favorito 
     */
    private void selecionarAssunto(Favorito favorito) {
        comboBoxAssuntos.setSelectedItem(favorito.getAssunto());
    }
    
    /**
     * Fecha esta janela informando isso para a tela principal.
     */
    private void fechar() {
        principal.fecharAlteracao();
        this.dispose();
    }
    
    /**
     * Mostra logotipo.
     */
    private void logo() {
        try {
            new Global().logo(this);
        } catch (Exception exception) {
            new Erro("Erro ao mostrar o logotipo").setVisible(true);
        }
    }

